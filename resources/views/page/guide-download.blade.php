    <h1 style="font-weight:200">How to use OpenLand Contracts?</h1>
    <h2>Introduction</h2>
    <h2>How to Guides</h2>
    <p>ResourceContracts temporary is a repository of publicly available oil, gas, and mining contracts. The repository features plain language summaries of each contract’s key social, environmental, human rights, fiscal, and operational terms, and tools for searching and comparing contracts. ResourceContracts.org promotes greater transparency of investments in the extractive industries, and facilitates a better understanding of the contracts that govern them. Contracts that govern the relationship between governments and oil, gas, and mining companies in the extraction and exploitation of natural resources are complex. The related licenses, leases, concessions, and other contractual arrangements affect everything from tax liabilities to local content obligations to the protection of the environment. Despite the critical role these contracts play in setting the rules for investments in extractive industries, they are often difficult to discover. Even when publicly available, the tools to assess and compare the key provisions of such contracts are limited. This can result in a critical lack of knowledge for governments as they try to negotiate the best terms for their citizens, and can result in missed opportunities to learn from others’ past successes or missteps.</p>
