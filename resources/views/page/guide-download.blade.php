    <h1>OpenLand Contracts - How to Guides</h1>
    <p>OpenLandContracts is an online repository of publicly available contracts and related documents for agriculture, forestry, and other land-based investment projects. The site was created by the Columbia Center on Sustainable Investment (CCSI) and its partners to promote greater transparency of land-based investments and facilitate a better understanding of the contracts that govern them.</p>
    <p>To search, view and download documents in the repository and to learn more about using the site, visit openlandcontracts.org.</p>
    <h2>How to Guides</h2>
