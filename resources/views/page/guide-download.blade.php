                <h3>View a Document</h3>
                <ul>
                    <li>To view a specific document, click on the <strong>document title</strong> to go to the View Document page.</li>
                    <li>You can scroll through <strong>the full PDF document</strong> page by page, or click the <strong> &ldquo;TEXT&rdquo; button  </strong>to switch to a text- only view.</li>
                    <li>To search for specific words in the text, use the  <strong> &ldquo;Search in this document&rdquo;  </strong> box on the left. Note that text versions are created automatically and may contain errors.</li>
                    <li>OpenLandContracts.org features <strong>Annotations </strong> (i.e. plain-language summaries) on the left of the page that explain key provisions throughout the document.</li>
                    <li>To  <strong>download or share </strong> the document, use the buttons above the right column.</li>
                    <li>To view  <strong>key document details </strong> such as Associated Documents, the original source website, or document metadata, switch to the Summary Page using <strong>the green button on the top right of the page</strong>.</li>
