                <ul>
                    <li>To search OpenLandContracts, enter any word or phrase in the <strong>Search Box</strong> at the top of most pages.</li> 
                    <li>To narrow your search, use the advanced <strong>Search Tools</strong> to limit the results shown. Select from the options in each category (Country, Resource, Year Signed, etc.) and click the green Search button to narrow your search. You can <strong>select multiple categories—</strong>and multiple options within each category—to further narrow the results.</li>
                    <li>To show only <strong>recent documents</strong> (past 90 days) or only contracts with <strong>Annotations</strong>, use the checkboxes in the Search Tools. See more about Annotations below.</li>
