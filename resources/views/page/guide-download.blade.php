                <h3>Compare Using Clips Tool</h3>
                <ul>
                    <li>You can <strong>compare the text of terms in different contracts</strong>, by using the <strong>Clips tool</strong> to select, view, or download multiple Annotations from multiple contracts.</li>
                    <li>Click the [icon] paper clip button at the top right of any contract page to turn on the Clips function, then <strong>select any Annotation</strong> to add it to your saved Clips. You can <strong>continue adding Clips from any document</strong> as long as the Clips tool is on.</li>   
                    <li>To review your selected Clips, click the blue “View Clips” button to go to the <strong>All Clips</strong> page. You can sort and share this list of Clips like any other OpenLandContracts list.</li>
                    <li>To make it easier to compare terms, the <strong>All Clips</strong> page also offers enhanced print and download bundles. Select <strong>Print</strong> to create a document with the full text of all the Annotations in your Clips. Select <strong>Download</strong> to save a file including the document with the full Annotation texts, a spreadsheet with text, metadata and URLs for each Annotation, and a PDF that compiles the contract pages that correspond to each Annotation you clipped.</li>
                    <li>To <strong>quickly compare a small number of Annotations</strong> on your screen, you can also use the red [icon] View buttons in the last column of the All Clips page. Click the buttons to display each relevant contract page as a pop-up window. If you want to compare more than four or five contract provisions, we recommend using the Download button on the All Clips page instead.</li>
                </ul>
