                <h3>Find Documents by Country or Resource</h3>
                <ul>
                    <li>OpenLandContracts offers quick access to documents from a specific country or related to a specific resource (e.g., timber, palm oil). Use the global <strong>sidebar menu</strong> or the world map on the homepage to view by Country or by Resource.</li>
                    <li>On each Country page, you can <strong>sort</strong> documents by title, by year of signature, by resource, or by document type. In the right column, you’ll also find links to view country documents for specific resources. (Each Resource page has the same features.)</li>
                    <li>Use the search box <strong>immediately above the results</strong> to narrow your search using specific words or phrases.</li> 
                    <li>You can download country lists, resource lists, and search results using the <strong>Download button</strong> above the list on the right. The downloaded spreadsheet will contain the title, details, and web address (URL) for every contract and document displayed. </li>
                    <li>These pages to view by Country or Resource are available for easy browsing, but you can also use the main Search Box to search on the same terms.</li>
                </ul>
